//Potenciar un numero

var pep = Math.pow(2, 3);

console.log(pep)

//Redondear un numero

// Round = redondea al decimal mas proximo 0.49 = 0 y 0.50 = 1

var pep2 = Math.round(0.49);

console.log(pep2);

//Floor = redondea el numero decimal al entero de menor valor

var pep3 = Math.floor(5.93);

console.log(pep3);

//Ceil = Redondea al contrario del anterior, redondea al entero de mayor valor

var pep4 = Math.ceil(3.27);

console.log(pep4);

//MAX y MIN nos permitiran conocer el valor maximo o minimo de un conjunto de numeros

var ho = Math.max(1,2,3,4,5);
var ho2 = Math.min(1,2,3,4,5);

console.log(ho);
console.log(ho2);

//Random = nos permite generar un numero aleatorio y puede ser cualquiera entre 0 y 1

var mn = Math.random();

console.log(mn);